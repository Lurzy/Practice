# Ввод и проверка размера массива
N = int(input("Введите размер массива: "))
if N <= 0:
    print("Ошибка: размер массива не может быть отрицательным или равен нулю")
    exit()  # завершение программы если N <= 0

# Ввод элементов массива
A = []
print("Введите элементы массива:")
for i in range(N):
    A.append(int(input(f"A[{i}] = ")))

# Ввод числа B
B = int(input("Введите число B: "))

# Инициализация переменных
count = 0
product = 1

# Цикл для перебора элементов массива
for num in A:
    if num > B:
        count += 1
        product *= num

# Вывод результатов
print(f"Количество элементов больше {B}: {count}")
if count > 0:
    print(f"Произведение элементов больше {B}: {product}")
else:
    print("Нет элементов больше заданного числа")